import Foundation
import UIKit

extension UIColor {
  class func gray() -> UIColor {
    return UIColor(red: (64/255.0), green: (49/255.0), blue: (45/255.0), alpha: 1.0)
  }
  
  class func green() -> UIColor {
    return UIColor(red: (39/255.0), green: (174/255.0), blue: (96/255.0), alpha: 1.0)
  }
  
  class func red() -> UIColor {
    return UIColor(red: (192/255.0), green: (57/255.0), blue: (43/255.0), alpha: 1.0)
  }
}